package bean;

public class ErrorHoaBean {
	private static final long serialVersionUID = 1L;
	private String errId;
	private String errTen;
	private String errSoLuong;
	private String errGia;
	public String getErrId() {
		return errId;
	}
	public void setErrId(String errId) {
		this.errId = errId;
	}
	public String getErrTen() {
		return errTen;
	}
	public void setErrTen(String errTen) {
		this.errTen = errTen;
	}
	public String getErrSoLuong() {
		return errSoLuong;
	}
	public void setErrSoLuong(String errSoLuong) {
		this.errSoLuong = errSoLuong;
	}
	public String getErrGia() {
		return errGia;
	}
	public void setErrGia(String errGia) {
		this.errGia = errGia;
	}
}
